"use strict";
// creating a function with parameters which return a values in stringtsc 
function city_country(city, country) {
    return `${city} ${country}`;
}
//calling the function and print the return value
city_country('karachi', 'pakistan');
console.log(city_country('karachi', 'pakistan'));
console.log(city_country('dehli', 'india'));
console.log(city_country('toronto', 'USA'));
